const { PLANNER_ACTIONS } = require("./planner-action-types");
const { PLANNER_COMMAND_TYPES } = require("./planner-command-types");

const RESCUE_ROUTE_COMMAND_TYPES = Object.freeze({
  [PLANNER_ACTIONS.RESCUE_ABORTED]: PLANNER_COMMAND_TYPES.TASK_RESCUE_ABORTED,
  [PLANNER_ACTIONS.RESCUE_CLOSED_LATER]: PLANNER_COMMAND_TYPES.TASK_RESCUE_CLOSED_LATER,
  [PLANNER_ACTIONS.RESCUE_STARTED]: PLANNER_COMMAND_TYPES.TASK_RESCUE_STARTED,
  [PLANNER_ACTIONS.RESCUE_SHIFT_RECORDED]: PLANNER_COMMAND_TYPES.TASK_RESCUE_SHIFT_RECORDED,
  [PLANNER_ACTIONS.RESCUE_COMPLETED]: PLANNER_COMMAND_TYPES.TASK_RESCUE_COMPLETED,
});

const TASK_TUNING_ROUTE_COMMAND_TYPES = Object.freeze({
  [PLANNER_ACTIONS.SET_TODAY]: PLANNER_COMMAND_TYPES.TASK_SET_TODAY,
  [PLANNER_ACTIONS.UNSET_TODAY]: PLANNER_COMMAND_TYPES.TASK_UNSET_TODAY,
  [PLANNER_ACTIONS.SET_VITAL]: PLANNER_COMMAND_TYPES.TASK_SET_VITAL,
  [PLANNER_ACTIONS.UNSET_VITAL]: PLANNER_COMMAND_TYPES.TASK_UNSET_VITAL,
  [PLANNER_ACTIONS.SET_URGENCY]: PLANNER_COMMAND_TYPES.TASK_SET_URGENCY,
  [PLANNER_ACTIONS.SET_RESISTANCE]: PLANNER_COMMAND_TYPES.TASK_SET_RESISTANCE,
  [PLANNER_ACTIONS.SET_DEADLINE]: PLANNER_COMMAND_TYPES.TASK_SET_DEADLINE,
  [PLANNER_ACTIONS.SET_HEAT_ZONE]: PLANNER_COMMAND_TYPES.TASK_SET_HEAT_ZONE,
  [PLANNER_ACTIONS.MARK_NOT_YOUR_MOVE]: PLANNER_COMMAND_TYPES.TASK_MARK_NOT_YOUR_MOVE,
  [PLANNER_ACTIONS.CLEAR_NOT_YOUR_MOVE]: PLANNER_COMMAND_TYPES.TASK_CLEAR_NOT_YOUR_MOVE,
  [PLANNER_ACTIONS.SET_CHECKIN]: PLANNER_COMMAND_TYPES.TASK_SET_CHECKIN,
});

const RESCUE_ROUTE_TYPES = Object.freeze(Object.keys(RESCUE_ROUTE_COMMAND_TYPES));
const TASK_TUNING_ROUTE_TYPES = Object.freeze(Object.keys(TASK_TUNING_ROUTE_COMMAND_TYPES));

function getRescueCommandType(routeType = "") {
  return RESCUE_ROUTE_COMMAND_TYPES[String(routeType || "")] || "";
}

function getTaskTuningCommandType(routeType = "") {
  return TASK_TUNING_ROUTE_COMMAND_TYPES[String(routeType || "")] || "";
}

module.exports = {
  RESCUE_ROUTE_COMMAND_TYPES,
  RESCUE_ROUTE_TYPES,
  TASK_TUNING_ROUTE_COMMAND_TYPES,
  TASK_TUNING_ROUTE_TYPES,
  getRescueCommandType,
  getTaskTuningCommandType,
};
